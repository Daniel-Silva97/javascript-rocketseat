/*
Variáveis

-  São nomes simbólicos para receber algum valor
- Atalhos de código
- Identificadores
- Temos 3 palavras reservadas para criar uma variável:
    * var
    * let
    * const

*/

//Criando uma variável var

var clima = "Quente"

//Mostrando a variavel com console log

console.log(clima)


//Alterando o valor da variável

clima = "Frio"

//Mostrando a variável após alterar

console.log(clima)




//Criando uma variável let

let carro = "Astra"

//Mostrando a variável let

console.log(carro)

//Alterando o valor da variável let

carro = "Fox"

//Mostrando o novo valor

console.log(carro)

/*
Variável const diferente das outras, o valor não pode ser modificado.

Após declarada, o valor será permanente.

Exemplo:

*/

//Criando variável const

const objeto = "Estante"


//Mostrando o valor da variável

console.log(objeto)

//Tentando alterar o valor (Não pode, apenas para mostrar o erro)

objeto = "Mesa"

//Mostrando o erro

console.log(objeto)
