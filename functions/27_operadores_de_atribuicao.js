// Operadores de atribuição

let x

console.log(x)


// Assignment
x = 1 // Aqui estou atribuindo (assignment) a x o valor 1
console.log(x)
 

// Addition assignment
x += 2 // Aqui irei somar a x o valor de 2, convertendo para uma expressão mais simples, isto é igual a fazer: x = x + 2

console.log(x)

// Subtraction assignment
x -= 1 // Mesma lógica, porém subtraindo o valor, equivale a  x = x - 1

console.log(x)

// Multiplication assignment
x *= 2 //Agora para multiplicação, equivale a x = x * 1

console.log(x)


// Division assignment
x /= 2 // Para divisão, equivale a x = x / 2

console.log(x)

// Remainder, exponentiation

//Remainder
x %= 2  // equivale a x = x % 2
console.log(x)

// Exponentiation
x **= 2 //Equivale a x = x ** 2
console.log(x)

