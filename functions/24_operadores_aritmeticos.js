//Operadores Aritméticos

//Multiplicação
console.log(2 * 2)

//Divisão
console.log( 12 / 2)

//Soma
console.log(16 + 16)

//Subtração
console.log(34 - 23)


//Resto da divisão
let remainder //Remainder significa resto da divisão
remainder = 11 % 3 // % siginifica resto de divisão no JS

console.log(remainder)

//Incremento
let increment = 0
increment++ // incluindo +1
increment++
console.log(increment)



console.log(++increment) //Incrementa +1 antes de mostrar no console.log
console.log(increment++) //Incrementa após este console.log, então no próximo console.log mostraria o resultado somente
console.log(increment) //Resultado do incremento anterior


//Decremento

let decrement = 0
decrement-- // Fazendo -1
decrement--

console.log(decrement)


console.log(decrement--) //Mesma lógica incremento
console.log(decrement)

console.log(--decrement) //Mesma lógica incremento

//Exponencial (**)

console.log(3 ** 3) //3 elevado a 3

