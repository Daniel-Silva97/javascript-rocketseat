/*
Prototype

    * prototype-based language - Significa que o JS é uma liguagem baseada em protótipo
    * prototype chain
    * __proto__

__proto__

Para explicar o conceito de protótipo vou utilizar a criação de um objeto.
Quando criamos o objeto {}, ele herdará as propriedades de um objeto ascedente (Criado anteriormente) para este novo objeto, em casos de objeto, o protótipo usado é __proto__ podemos ver isso pelo console quando expandido o objeto, no canto inferior do objeto aparecerá o nome do protótipo utilizado.


Posso usar o proto para string também que vai ter suas propriedades fazendo assim:
"Daniel".__proto__

Dessa forma o __proto__ vai criar um objeto de nome String com varias propriedades e valores para ela.

O conceito de como usar é básico mesmo, basta pegar qualquer coisa que queira fazer no código e com um ".", vc pode incluir a propriedade desejada na frente da sua função, string, objeto, etc...
*/



